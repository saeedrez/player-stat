CREATE TABLE PLAYER_STAT(
	ID INT AUTO_INCREMENT,
	RANKING INT,
	SEASON_ID INT,
	SEASON_NAME VARCHAR(255),
	TOURNEMENT_ID INT,
	TOURNEMENT_REGION_ID INT,
	TOURNEMENT_REGION_CODE VARCHAR(255),
	REGION_CODE VARCHAR(255),
	TOURNEMENT_NAME VARCHAR(255),
	TOURNEMENT_SHORT_NAME VARCHAR(255),
	FIRST_NAME VARCHAR(255),
	LAST_NAME VARCHAR(255),
	PLAYER_ID INT,
	IS_ACTIVE BOOLEAN,
	IS_OPTA BOOLEAN,
	TEAM_ID INT,
	TEAM_NAME VARCHAR(255),
	PLAYED_POSITIONS VARCHAR(255),
	AGE INT,
	HEIGHT INT,
	WEIGHT INT,
	POSITION_TEXT VARCHAR(255),
	APPS INT,
	SUB_ON INT,
	MINS_PLAYED INT,
	RATING DOUBLE,
	GOAL INT,
	ASSIST_TOTAL INT,
	YELLOW_CARD INT,
	RED_CARD INT,
	SHOTS_PER_GAME DOUBLE,
	AERIAL_WON_PER_GAME DOUBLE,
	MAN_OF_THE_MATCH INT,
	NAME VARCHAR(255),
	IS_MAN_OF_THE_MATCH BOOLEAN,
	PLAYED_POSITIONS_SHORT VARCHAR(255),
	PASS_SUCCESS DOUBLE
);